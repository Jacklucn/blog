                        <div class="post-content markdown"><h2 id="工厂模式">工厂模式</h2>

                            <p>在函数中创建 Object 对象，并为对象添加属性。函数返回添加属性之后的对象。</p>

                            <pre><code class="language-js">function createPerson(name, age) {
  var o = new Object()
  o.name = name
  o.age = age
  o.sayName = function () {
    console.log(this.name)
  }
  return o
}

var p = createPerson(&quot;ahonn&quot;, 21)
</code></pre>

